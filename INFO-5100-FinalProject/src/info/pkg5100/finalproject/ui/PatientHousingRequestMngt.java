    private void btnAcceptRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptRequestActionPerformed
        try {
            // TODO add your handling code here:
            int selectedRowIndex = tblAvailablePatient.getSelectedRow();
            if (selectedRowIndex < 0) {
                JOptionPane.showMessageDialog(this, "Please select an incident.");
                return;
            }

            DefaultTableModel model = (DefaultTableModel) tblAvailablePatient.getModel();
            Patient patient = (Patient) model.getValueAt(selectedRowIndex, 0);

            //incidentCase.setOrgId(this.currentOrganization.getId());
            //incidentCase.setOrgType(this.currentOrganization.getType());
            patient.setHousingid(this.currentuser.getId());
            patient.setHousingPeriod(Integer.valueOf(jcbHousindDays.getSelectedItem().toString()));

            this.patientDaoImplementation.update(patient);
            populateAcceptedCaseTable();
            populateRequestsTable();

        } catch (SQLException ex) {
            Logger.getLogger(PatientHousingRequestMngt.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnAcceptRequestActionPerformed

    void populateAcceptedCaseTable() throws SQLException {
        DefaultTableModel model = (DefaultTableModel) tblAcceptedPatients.getModel();
        model.setRowCount(0);

        for (Patient patient : this.patientDaoImplementation.getPatientsByLocationAndConvicted(currentOrganization.getLocation(), "Innocent")) {

            if (patient.getHousingid() == this.currentuser.getId() && patient.getHousingPeriod() > -1) {
                Object[] row = new Object[4];
                row[0] = patient;
                row[1] = patient.getName();
                row[2] = patient.getHousingPeriod();
                row[3] = patient.getIsConvicted();

                model.addRow(row);
            }
        }
    }

    void populateRequestsTable() throws SQLException {
        DefaultTableModel model = (DefaultTableModel) tblAvailablePatient.getModel();
        model.setRowCount(0);

        for (Patient patient : this.patientDaoImplementation.getPatientsByLocationAndConvicted(currentOrganization.getLocation(), "Innocent")) {

            if (patient.getHousingPeriod() < 0) {
                Object[] row = new Object[4];
                row[0] = patient;
                row[1] = patient.getName();
                row[2] = patient.getIsConvicted();
                row[3] = patient.getIncidentCaseId();

                model.addRow(row);
            }
        }
    }

